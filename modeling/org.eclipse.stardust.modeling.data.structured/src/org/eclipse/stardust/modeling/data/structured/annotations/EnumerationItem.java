package org.eclipse.stardust.modeling.data.structured.annotations;

public class EnumerationItem
{
   private String name;
   private String value;

   public EnumerationItem(String value)
   {
      this(value, value);
   }

   public EnumerationItem(String name, String value)
   {
      this.value = value == null ? "" : value; //$NON-NLS-1$
      this.name = name == null || name.length() == 0 ? this.value : name;
   }

   public String toString()
   {
      return name;
   }

   public String getValue()
   {
      return value;
   }

   public int hashCode()
   {
      final int prime = 31;
      int result = 1;
      result = prime * result + ((name == null) ? 0 : name.hashCode());
      result = prime * result + ((value == null) ? 0 : value.hashCode());
      return result;
   }

   public boolean equals(Object obj)
   {
      if (this == obj)
      {
         return true;
      }
      if (obj == null)
      {
         return false;
      }
      if (getClass() != obj.getClass())
      {
         return false;
      }
      final EnumerationItem other = (EnumerationItem) obj;
      if (name == null)
      {
         if (other.name != null)
         {
            return false;
         }
      }
      else if (!name.equals(other.name))
      {
         return false;
      }
      if (value == null)
      {
         if (other.value != null)
         {
            return false;
         }
      }
      else if (!value.equals(other.value))
      {
         return false;
      }
      return true;
   }
}
